_raid_stamina = {'1': 600, '2': 1800, '3': 3600, '4': 9000, '5': 15000, '6': 22500}

def populate_boss_list():
    boss_list = []
    boss_level_dict = {'1': [], '2': [], '3': [], '4': [], '5': [] }
    def walk_siblings(sib):
        try:
            # We've encountered another level header. Exit this loop.
            if sib.attrs["class"] == ['raid-boss-tier-wrap']:
                return False
        except:
            pass
        boss = None
        try:
            boss = sib.find('div', attrs={'class': 'pokemonOption'})
        except:
            pass
        if boss:
            poke = boss.attrs['data-pokemon-slug']
            if '-' in poke:
                if 'alola' in poke or 'galar' in poke:
                    poke = poke.replace('alola', 'alolan')
                    poke = poke.replace('galar', 'galarian')
                    poke_split = poke.split('-')[::-1]
                    poke = ' '.join(poke_split)
                else:
                    poke = poke.replace('-incarnate', '')
                    poke = poke.replace('-armor', '')
            return poke


    task_page = "https://thesilphroad.com/raid-bosses"
    page = requests.get(task_page)
    soup = BeautifulSoup(page.content, 'html.parser')
    # This will pull all tier headers to walk through.
    # The headers are at the same hierarchy level as the actual raids
    # so walking through siblings will encounter both.
    tiers = soup.findAll('div', attrs={'class': 'raid-boss-tier-wrap'})
    for tier in tiers:
        tier_str = tier.find('h4')
        if 'EX' in tier_str.string:
            level = 'EX'
        else:
            level = tier_str.string.split(' ')[1]
        raid_pokemon = []
        for sibling in tier.next_siblings:
            result = walk_siblings(sibling)
            if result:
                raid_pokemon.append(result)
            # If result is false, it means we encountered a header.
            # Break the inner loop and save the current Pokemon list
            # to the current level.
            if result == False:
                break
        if level != 'EX':
            boss_level_dict[level] += raid_pokemon
        else:
            boss_level_dict['5'] += raid_pokemon
        boss_list += raid_pokemon

    for b in boss_list:
        if b.lower().startswith('alolan'):
            boss_list.append(b.split()[1])
        if b.lower().startswith('galar'):
            boss_list.append(b.split()[1])
        if b.lower().startswith('incarnate'):
            boss_list.append(b.split()[1])
        if b.lower().startswith('armor'):
            boss_list.append(b.split()[1])
    return boss_list, boss_level_dict

def calculate_boss_cp_list(boss_dict):
    boss_cp_map = {}
    for tier in boss_dict.keys():
        for poke in boss_dict[tier]:
            poke = poke.lower()
            if poke in _poke_stats:
                stamina = _raid_stamina[tier]
                attack = _poke_stats[poke][0]
                defense = _poke_stats[poke][1]
                boss_cp = int(((attack + 15) * math.sqrt(defense + 15) * math.sqrt(stamina)) / 10)
                boss_cp_map[str(boss_cp)] = poke
    return boss_cp_map

